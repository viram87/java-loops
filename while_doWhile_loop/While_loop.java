//while loop

package while_doWhile_loop;

public class While_loop {

	public static void main(String[] args) {

		// case 1
		System.out.println("case 1");
		int i = 1;

		while (i < 100) {
			System.out.println(i);
			i = i * 2;
		}

		System.out.println(" ");

		// case 2
		System.out.println("case 2");

		int j = 100;

		while (j < 100) {
			System.out.println(j);
			j = j * 2;
		}

	}

}
